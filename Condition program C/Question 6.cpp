//Vowel or not
//vowel=a,e,i,o,u
#include<stdio.h>

int main(){
	
	char alphabet;
	
	printf("Enter the character:");
	scanf("%c",&alphabet);
	
	if(alphabet=='a'){
		printf("It is a vowel");
		
	}else if(alphabet=='e'){
		
		printf("It is a vowel");
	}else if(alphabet=='i'){
		printf("It is a vowel");
	}else if(alphabet=='o'){
		printf("It is a vowel");
	}else if(alphabet=='u'){
		printf("It is a vowel");
	}else{
		printf("It is not a vowel");
	}

}